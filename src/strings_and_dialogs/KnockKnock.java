package strings_and_dialogs;

import javax.swing.JOptionPane;

public class KnockKnock {
	public static void main(String[] args) {
		JOptionPane.showMessageDialog(null, "Knock,knock");
		JOptionPane.showMessageDialog(null, "Who's there?");
		JOptionPane.showMessageDialog(null, "Amos");
		JOptionPane.showMessageDialog(null, "Amos who?");
		JOptionPane.showMessageDialog(null, "A mosquito just bit me.");

	}

}
